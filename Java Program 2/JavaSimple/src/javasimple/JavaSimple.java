package javasimple;

public class JavaSimple {

    public static void main(String[] args) {
        
        int c = 5;
        System.out.println(c);
        System.out.println(c++);
        System.out.println(c);
        
        System.out.println("\n\n\n");
        
        int a = 3;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        
        System.out.println("\n\n\n");
        
        
        System.out.println(c);
        System.out.println(c--);
        System.out.println(c);
        
        System.out.println("\n\n\n");
        
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);
        
        System.out.printf("\n\n%d\n\n",a);
    }
    
}
